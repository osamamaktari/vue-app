<script>
import SidebarToggleComponent from './SidebarToggleComponent.vue';  // استيراد مكون التوغل
import SidebarlinksComponent from './SidebarlinksComponent.vue';
export default {
  components: {
    SidebarlinksComponent,
    SidebarToggleComponent
  },
  data() {
    return {
      isCollapsed: false,
      isMobile: window.innerWidth < 1024,
    };
  },
  mounted() {
    window.addEventListener('resize', this.handleResize);
    this.handleResize();
  },
  beforeUnmount() {
    window.removeEventListener('resize', this.handleResize);
  },
  methods: {
    handleResize() {
      this.isMobile = window.innerWidth < 1024;
      if (this.isMobile) this.isCollapsed = true;
      else this.isCollapsed = false;
    },
    toggleSidebar() {
      this.isCollapsed = !this.isCollapsed;
    },
  },
  watch: {
    $route() {
      if (this.isMobile) this.isCollapsed = true;
    }
  }
}
</script>   
